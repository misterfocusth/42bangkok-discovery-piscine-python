#!/usr/bin/env python3

num1 = int(input("Give me the first number: "))
num2 = int(input("Give me the second number: "))
print("Thank you!")

add = num1 + num2
sub = num1 - num2
div = int(num1 / num2)
mul = num1 * num2

print(f"{num1} + {num2} = {add}")
print(f"{num1} - {num2} = {sub}")
print(f"{num1} / {num2} = {div}")
print(f"{num1} * {num2} = {mul}")
